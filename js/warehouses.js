// Функція пошуку в локальній базі
function getLocalWarehouses(cityName, query, deliveryType) {
  const isPoshtamat = deliveryType === 'nova_poshta_poshtamat';

  // Шукаємо місто (нечутливо до регістру і часткове співпадіння)
  const cityKey = Object.keys(NP_LOCAL_WAREHOUSES).find(k =>
    k.toLowerCase() === cityName.toLowerCase() ||
    cityName.toLowerCase().startsWith(k.toLowerCase()) ||
    k.toLowerCase().startsWith(cityName.toLowerCase())
  );

  if (!cityKey) return null; // null = немає в локальній базі

  let warehouses = NP_LOCAL_WAREHOUSES[cityKey];

  // Фільтруємо по типу
  if (isPoshtamat) {
    warehouses = warehouses.filter(w => w.toLowerCase().includes('поштомат'));
  } else if (deliveryType === 'nova_poshta') {
    warehouses = warehouses.filter(w => w.toLowerCase().includes('відділення'));
  }

  // Фільтруємо по пошуковому запиту
  if (query && query.trim()) {
    const q = query.toLowerCase();
    warehouses = warehouses.filter(w => w.toLowerCase().includes(q));
  }

  return warehouses;
}